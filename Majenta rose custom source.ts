  async getAnime(ids: number[]): Promise<$app.AL_BaseAnime[]> {
    const ret: $app.AL_BaseAnime[] = []
    for (const id of ids) {
      if (anime[id]) {
        const a = $clone(anime[id]) as any
        delete a["relations"]
        ret.push(a)
      }
    }
    return ret
  }

